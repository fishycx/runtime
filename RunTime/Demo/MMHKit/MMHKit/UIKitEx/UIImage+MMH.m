- (UIImage *)_yy_flipHorizontal:(BOOL)horizontal vertical:(BOOL)vertical {
    if (!self.CGImage) return nil;
    size_t width = (size_t)CGImageGetWidth(self.CGImage);
    size_t height = (size_t)CGImageGetHeight(self.CGImage);
    size_t bytesPerRow = width * 4;
    CGColorSpaceRef colorSpace = CGColorSpaceCreateDeviceRGB();
    CGContextRef context = CGBitmapContextCreate(NULL, width, height, 8, bytesPerRow, colorSpace, kCGBitmapByteOrderDefault | kCGImageAlphaPremultipliedFirst);
    CGColorSpaceRelease(colorSpace);
    if (!context) return nil;
    
    CGContextDrawImage(context, CGRectMake(0, 0, width, height), self.CGImage);
    UInt8 *data = (UInt8 *)CGBitmapContextGetData(context);
    if (!data) {
        CGContextRelease(context);
        return nil;
    }
    vImage_Buffer src = { data, height, width, bytesPerRow };
    vImage_Buffer dest = { data, height, width, bytesPerRow };
    if (vertical) {
        vImageVerticalReflect_ARGB8888(&src, &dest, kvImageBackgroundColorFill);
    }
    if (horizontal) {
        vImageHorizontalReflect_ARGB8888(&src, &dest, kvImageBackgroundColorFill);
    }
    CGImageRef imgRef = CGBitmapContextCreateImage(context);
    CGContextRelease(context);
    UIImage *img = [UIImage imageWithCGImage:imgRef scale:self.scale orientation:self.imageOrientation];
    CGImageRelease(imgRef);
    return img;
}

- (UIImage *)centerSquareImage {
    CGImageRef cg = self.CGImage;
    size_t width = CGImageGetWidth(cg);
    size_t height = CGImageGetHeight(cg);
    size_t length = MIN(width, height);
    CGRect rect = CGRectMake(((width / 2.0f) - (length / 2.0f)), ((height / 2.0f) - (length / 2.0f)), length, length);
    CGImageRef newCG = CGImageCreateWithImageInRect(cg, rect);
    UIImage *image = [UIImage imageWithCGImage:newCG scale:kScreenScale orientation:self.imageOrientation];
    CGImageRelease(newCG);
    return image;
}

+ (UIImage *)resizedImageToFitUploadSizeOfImage:(UIImage *)originalImage {
    return  [self resizedImageToFitUploadSizeOfImage: originalImage scale:1.0f];
}

+ (UIImage *)resizedImageToFitUploadSizeOfImage:(UIImage *)originalImage scale:(CGFloat)scale {
    CGFloat width = originalImage.size.width;
    CGFloat height = originalImage.size.height;
    
    if (width < height) {
        [self resizeSmallSide:&width bigSide:&height];
    } else {
        [self resizeSmallSide:&height bigSide:&width];
    }
    
    CGSize newSize = CGSizeMake(width, height);
    
    UIGraphicsBeginImageContext(CGSizeMake(width, height));
    [originalImage drawInRect:CGRectMake(0, 0, newSize.width, newSize.height)];
    UIImage* resizedImg = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    UIImage *img = nil;
    
    NSData *imageData = UIImageJPEGRepresentation(originalImage, scale);
    NSData *resizedImgData = UIImageJPEGRepresentation(resizedImg, scale);
    if (resizedImgData.length < imageData.length) {
        img = [UIImage imageWithData: resizedImgData];
    } else {
        img =[UIImage imageWithData: resizedImgData];
    }
    
    return img;
}

